/* Paste graphics of background where is free space */
void paste_graphics() {
    for (int y = 0; y < BOARD_HEIGHT; y++) {
        for (int x = 0; x < BOARD_WIDTH; x++) {
            if (board_values[y][x] != SNAKE && board_values[y][x] != OBSTACLE && board_values[y][x] != APPLE) {
                board_values[y][x] = background_board[y][x];
            }
        }
    }
}

/* set board template to board_values */
void init_board(int map) {
    unsigned char (*map_ptr)[BOARD_WIDTH];
    switch (map) {
        case 0:
            map_ptr = MAP_CLASSIC;
            break;
        case 1:
            map_ptr = MAP_BORDER;
            break;
        case 2:
            map_ptr = MAP_MAZE;
            break;
        default:
            return;
    }
    for (int y = 0; y < BOARD_HEIGHT; y++) {
        for (int x = 0; x < BOARD_WIDTH; x++) {
            board_values[y][x] = map_ptr[y][x];
        }
    }
}
